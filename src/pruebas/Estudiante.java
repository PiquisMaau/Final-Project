    private void jbtnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnGuardarActionPerformed
        // TODO add your handling code here:
        Conexion cc = new Conexion();
        Connection cn = cc.conectar();
        String sql = "insert into estudiantes values(?,?,?,?,?)";
        try {
            if(jtxtCedula.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "Ingrese al cedula");
                jtxtCedula.requestFocus();
            }
            else if(jtxtNombre.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "Ingrese el nombre");
                jtxtNombre.requestFocus();
            }
            else if(jtxtApellido.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "Ingrese el apellido");
                jtxtApellido.requestFocus();
            }
            else{
                PreparedStatement psd = cn.prepareStatement(sql);
                psd.setString(1, jtxtCedula.getText());
                psd.setString(2, jtxtNombre.getText());
                psd.setString(3, jtxtApellido.getText());
                if(jtxtEdad.getText().isEmpty()){
                    psd.setString(4, "0");   
                }
                else{
                    psd.setString(4, jtxtEdad.getText());
                }
                if(jtxtTelefono.getText().isEmpty()){
                    psd.setString(5, "000000000");   
                }
                else{
                    psd.setString(5, jtxtTelefono.getText());
                }
                int com = psd.executeUpdate();
                if (com > 0) {
                    JOptionPane.showMessageDialog(null, "Se inserto correctamente");
                    SeleccionarEstudiante();
                    activarBotones();
                    desactivarTextos();
                    limpiarTextos();
                }
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_jbtnGuardarActionPerformed

