    public void SeleccionarEstudiante() {
        Conexion cc = new Conexion();
        Connection cn = cc.conectar();
        String sql = "select * from estudiantes";
        try {
            String titulo[] = {"Cedula ", "Nombre ", "Apellido", "Edad ", "Telefono"};
            String datos[] = new String[5];
            modelo = new DefaultTableModel(null, titulo);
//            jtblEstudiantes.setModel(modelo);
            Statement psd = cn.createStatement();
            ResultSet rs = psd.executeQuery(sql);
            while (rs.next()) {
                datos[0] = rs.getString("estcedula");
                datos[1] = rs.getString("estnombre");
                datos[2] = rs.getString("estapellido");
                datos[3] = rs.getString("estedad");
                datos[4] = rs.getString("esttelefono");
                modelo.addRow(datos);
            }
            jtblEstudiantes.setModel(modelo);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }

    public void BorrarEstudiante() {
        Conexion cc = new Conexion();
        Connection cn = cc.conectar();
        String sql = "delete from estudiantes where estcedula='" + jtxtCedula.getText() + "'";
        try {
            PreparedStatement psd = cn.prepareStatement(sql);
            int com = psd.executeUpdate();
            if (com > 0) {
                JOptionPane.showMessageDialog(null, "Se borro");
                SeleccionarEstudiante();
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }

    public void actualizar() {
        Conexion cc = new Conexion();
        Connection cn = cc.conectar();
        String sql = "update estudiantes set estnombre ='" + jtxtNombre.getText()
                + "', estapellido = '" + jtxtApellido.getText()
                + "', estedad = '" + jtxtEdad.getText()
                + "', esttelefono = '" + jtxtTelefono.getText()
                + "'where estcedula='" + jtxtCedula.getText() + "'";
        try {
            PreparedStatement psd = cn.prepareStatement(sql);
            int com = psd.executeUpdate();
            if (com > 0) {
                JOptionPane.showMessageDialog(null, "Se actualizo correctamente");
                SeleccionarEstudiante();
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }
    
    public void seleccionarUnEstudiante(){
        jtblEstudiantes.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent e) {
                if(jtblEstudiantes.getSelectedRow()!=-1){}
                int fila = jtblEstudiantes.getSelectedRow();
                jtxtCedula.setText(jtblEstudiantes.getValueAt(fila, 0).toString());
                jtxtNombre.setText(jtblEstudiantes.getValueAt(fila, 1).toString());
                jtxtApellido.setText(jtblEstudiantes.getValueAt(fila, 2).toString());
                jtxtEdad.setText(jtblEstudiantes.getValueAt(fila, 3).toString());
                jtxtTelefono.setText(jtblEstudiantes.getValueAt(fila, 4).toString());
                activarBotonesActualizar();
                activarTextos();
            }
        });
    }
    
    public void activarBotones(){
        jbtnNuevo.setEnabled(true);
        jbtnGuardar.setEnabled(false);
        jbtnEditar.setEnabled(false);
        jbtnBorrar.setEnabled(false);
        jbtnCancelar.setEnabled(true);
    }
    
    public void activarBotonesNuevo(){
        if(jtblEstudiantes.getSelectedRow()!=-1){
        jbtnNuevo.setEnabled(false);
        jbtnGuardar.setEnabled(true);
        jbtnEditar.setEnabled(false);
        jbtnBorrar.setEnabled(false);
        jbtnCancelar.setEnabled(true);
        }
    }
    
    public void activarBotonesActualizar(){
            jbtnNuevo.setEnabled(false);
            jbtnGuardar.setEnabled(false);
            jbtnEditar.setEnabled(true);
            jbtnBorrar.setEnabled(true);
            jbtnCancelar.setEnabled(true);
    }
    
